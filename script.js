function startTouch(e) {
    // UNLOCK: Schaltet die Vibration für den Rest der Session frei
    if (navigator.vibrate) navigator.vibrate(0);
    lastInteractionTime = Date.now(); // Zeitstempel aktualisieren
    let touch = e.touches ? e.touches[0] : e;
    lastX = touch.clientX;
    lastY = touch.clientY;
    // Beim Auflegen des Fingers setzen wir den Vektor auf Null
    vectorX = 0; 
    vectorY = 0;
    
    // Wenn wir aus dem Standby kommen, starten wir mit der nächsten Phase
    if (sinusState === "STANDBY") {
        if (nextPhaseIsEinatmen) {
            sinusState = "EINATMEN";
            console.log(">> 1: EINATMEN START (Bernstein)");
        } else {
            sinusState = "AUSATMEN";
            console.log(">> 3: AUSATMEN START (Blau)");
        }
    }
    phaseStartTime = Date.now(); // Zeitmessung startet erst beim Auflegen
    
}

function moveTouch(e) {
    lastInteractionTime = Date.now();
    if (lastX === null || lastY === null) return;
    
    let touch = e.touches ? e.touches[0] : e;
    let dx = touch.clientX - lastX;
    let dy = touch.clientY - lastY;
    let distance = Math.sqrt(dx * dx + dy * dy);

    // Wenn der Finger sich kaum bewegt, "verblasst" die Vektor-Erinnerung langsam
    if (distance < 2) {
        vectorX *= 0.9; 
        vectorY *= 0.9;
    }

    if (distance > 4) {
        if (vectorX !== 0 || vectorY !== 0) {
            // Die mathematische Prüfung des Winkels (Skalarprodukt)
            // cosTheta = \frac{\vec{a} \cdot \vec{b}}{|\vec{a}| \cdot |\vec{b}|}
            let dotProduct = (dx * vectorX) + (dy * vectorY);
            let len1 = distance;
            let len2 = Math.sqrt(vectorX * vectorX + vectorY * vectorY);
            let cosTheta = dotProduct / (len1 * len2);

            if (cosTheta < -0.7) {
                togglePhase();
                vectorX = dx;
                vectorY = dy;
            } else {
                vectorX = (vectorX * 0.8) + (dx * 0.2);
                vectorY = (vectorY * 0.8) + (dy * 0.2);
            }
        } else {
            vectorX = dx;
            vectorY = dy;
        }
        lastX = touch.clientX;
        lastY = touch.clientY;
    }
}

function endTouch() {
   if (sinusState !== "STANDBY") {
        logPhaseChange(sinusState, "STANDBY");
    }

    lastX = null;
    lastY = null;
    vectorX = 0;
    vectorY = 0;
    
    // Bereite die nächste Phase vor
    if (sinusState === "EINATMEN") nextPhaseIsEinatmen = false;
    if (sinusState === "AUSATMEN") nextPhaseIsEinatmen = true;
    
    sinusState = "STANDBY";
    console.log(">> 0: STANDBY (Daten gespeichert)");
}

function togglePhase() {
   let now = Date.now();
    if (now - lastToggleTime < 1200) return; // Spam-Schutz

    let oldPhase = sinusState;
    sinusState = (sinusState === "EINATMEN") ? "AUSATMEN" : "EINATMEN";
    nextPhaseIsEinatmen = (sinusState === "EINATMEN");

    // Feedback: Ton & Vibration
    playPing(sinusState === "EINATMEN" ? 660 : 440);
    if (navigator.vibrate) {
        sinusState === "EINATMEN" ? navigator.vibrate(60) : navigator.vibrate([20, 40]);
    }

    lastToggleTime = now;
    logPhaseChange(oldPhase);
}

function startPhase() {
    if (nextPhaseIsEinatmen) {
        sinusState = "EINATMEN";
        console.log(">> 1: EINATMEN (Bernstein, Motor hoch)");
    } else {
        sinusState = "AUSATMEN";
        console.log(">> 3: AUSATMEN (Blau, Motor ab)");
    }
}

function endPhase() {
    // Sobald du loslässt, bereiten wir den Richtungswechsel vor
    if (sinusState === "EINATMEN") {
        nextPhaseIsEinatmen = false; // Nächster Tap wird Ausatmen
    } else if (sinusState === "AUSATMEN") {
        nextPhaseIsEinatmen = true;  // Nächster Tap wird wieder Einatmen
    }
     
    console.log(">> 0: PAUSE / STANDBY (Finger weg, Stille)");
}

